      subroutine  HDQPII   ( lda, n, a )
c     ==================================================================
c     ==================================================================
c     ====  HDQPII  -- initialize matrix to identity                ====
c     ==================================================================
c     ==================================================================
      
      integer            lda, n

      double precision   a( lda, * )

c     ==================================================================
      
      integer            i, j

      double precision   zero, one

      parameter        ( zero = 0.0d0, one = 1.0d0 )

c     ==================================================================

c     ... last modification  25-March-1996
      
c     ==================================================================

      do i = 1, n

         a (i,i) = one

         do j = 1, i-1

            a (i,j) = zero
            a (j,i) = zero

         enddo

      enddo
         
      return

c     end of  HDQPII

      end
